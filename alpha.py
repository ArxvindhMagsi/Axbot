    async def chat(self):
        """Start interactive chat session"""
        print("\nüè• Welcome to Medical AI Assistant!")
        print("Type 'quit' to exit, 'help' for commands")
        print("="*50)

        while True:
            try:
                query = input("\nüí¨ Your medical question: ").strip()

                if query.lower() == 'quit':
                    print("üëã Goodbye! Stay healthy!")
                    break

                if query.lower() == 'help':
                    print("\nAvailable commands:")
                    print("- Ask any medical question")
                    print("- Type 'quit' to exit")
                    print("- Type 'history' to see conversation history")
                    continue

                if query.lower() == 'history':
                    print(f"\nüìö Conversation History ({len(self.conversation_history)} items):")
                    for i, item in enumerate(self.conversation_history, 1):
                        print(f"{i}. Q: {item['query'][:50]}...")
                        print(f"   A: {item['response'][:50]}...")
                    continue

                if not query:
                    continue

                print("\nüîÑ Processing your medical query...")
                print("üîç Searching multiple medical databases...")

                # Process the query
                response = await self.agent.process_medical_query(query)

                # Display the response
                self.display_response(response)

                # Save to history
                self.conversation_history.append({
                    'query': query,
                    'response': response.answer,
                    'timestamp': datetime.now().isoformat(),
                    'confidence': response.confidence
                })

                # Display visualizations
                for viz in response.visualizations:
                    img_data = base64.b64decode(viz['image_base64'])
                    img = Image.open(io.BytesIO(img_data))
                    plt.figure(figsize=(10, 6))
                    plt.imshow(img)
                    plt.axis('off')
                    plt.title(viz['title'])
                    plt.show()

            except KeyboardInterrupt:
                print("\n\nüëã Session interrupted. Goodbye!")
                break
            except Exception as e:
                print(f"\n‚ùå Error: {e}")
                logger.error(f"Chat error: {e}")

