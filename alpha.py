@dataclass
class SearchResult:
    """Data class for search results"""
    source: str
    title: str
    content: str
    url: str
    relevance_score: float
    metadata: Dict[str, Any]
    image_url: Optional[str] = None

@dataclass
class AgentResponse:
    """Data class for agent responses"""
    answer: str
